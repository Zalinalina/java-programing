package MyPracticeApril;

public class loopquiz {
    public static void main(String[] args) {
        int a = 0;
        while (a <= 6) {
            a += 2;
        }
        System.out.println(a);



        int i = 0;
        do {
            i++;
            System.out.print(i + " ");
        }while(i<=4);

        int z = 5;
        for(int n = 5;n>0;n--){
            z+=n;
        }
        System.out.println(z);
    }
}
