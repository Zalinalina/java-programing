public class EmployeeInfo {
    public static void main(String[] args) {
        System.out.println("Company name:      Google");
        System.out.println("Employee:          Zalina");
        System.out.println("ID:                12345");
        System.out.println("Job title:         SDET");
        System.out.println("");
        System.out.println("");
        System.out.println("Company name:\tGoogle\nEmployee:\t\tZalina\nID:\t\t\t\t12345\nJob title:\t\tSDET");
    }
}
