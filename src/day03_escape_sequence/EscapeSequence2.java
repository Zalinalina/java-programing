package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"iphone case\", \"iphone\", \"xbox\", \"webcam\", \"monitor\"");
        System.out.println("\"iphone case\", \"iphone\",\n \"xbox\", \"webcam\", \"monitor\"");
        System.out.println("\"book\", \"pen\", \"notebook\", \"pencil\"");
        System.out.println("");
        System.out.println("    ^");
        System.out.println("  /   \\");
        System.out.println("  |   |");
        System.out.println("  -----");
        System.out.println();
        System.out.println("     \"\"\"    \"\"\"");
        System.out.println("   \" \t\"  \"\t\"\n  \"\t\t  \"  \t \"\n   \"\t\t\t \"\n\t\"\t\t    \"");
        System.out.println("      \"\t\t  \"\n\t\t\" \t\"\n\t\t  \"");

    }
}
