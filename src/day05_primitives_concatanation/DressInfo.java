package day05_primitives_concatanation;

public class DressInfo {
    public static void main(String[] args){
        String item = "Dress";
        String brand = "\"ZARA\"";
        String color = "blue";
        double price = 79.99;
        char size = 83;
        boolean trueToSize = true;

        System.out.println(item + " from new collection.");
        System.out.println("Brand:\t\t\t"+brand);
        System.out.println("Color:\t\t\t" + color);
        System.out.println("Price:\t\t\t$" + price);
        System.out.println("Size:\t\t\t" + size);
        System.out.println("True to size:\t" + trueToSize);


    }
}
