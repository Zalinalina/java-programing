package day05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args){
        String brand = "Rolex";
        String color = "Silver";
        double price = 15000;
        boolean waterResistant = true;
        boolean isSmart = false;
        char gender = 'F';

        System.out.println("Brand: \t\t\t\t\t" + brand);
        System.out.println("Color: \t\t\t\t\t" + color);
        System.out.println("Price: \t\t\t\t\t" + price);
        System.out.println("Water resistance: \t\t" + waterResistant);
        System.out.println("Is it Smatrt Watch? \t" + isSmart);
        System.out.println("Gender: \t\t\t\t" + gender);


    }
}
