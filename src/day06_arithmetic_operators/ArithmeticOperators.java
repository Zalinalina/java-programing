package day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main(String[] args){

        System.out.println(5+10);
        System.out.println(10-3);
        System.out.println(3*3);
        System.out.println(15 / 5);
        System.out.println(13 % 5); //3 remainder or ostatok


        System.out.println(-2 * -2); //4
        System.out.println(-2 * 2); //-4


        int chairs = 4 + 2;
        System.out.println(chairs);//6

        int spoons = 10 + 20 +12 + 0;
        System.out.println(spoons);//42

        int oranges = 25 % 3;
        System.out.println(oranges);

        // There are 42 spoons in the kitchen.
        System.out.println("There are " + spoons + " spoons in the kitchen.");

    }
}
